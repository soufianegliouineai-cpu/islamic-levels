// Premium Animation System
class PremiumAnimations {
  static fadeIn(element, duration = 300) {
    element.style.opacity = '0';
    element.style.transform = 'translateY(10px)';
    setTimeout(() => {
      element.style.transition = `opacity ${duration}ms ease, transform ${duration}ms ease`;
      element.style.opacity = '1';
      element.style.transform = 'translateY(0)';
    }, 10);
  }

  static slideUp(element, duration = 300) {
    element.style.transform = 'translateY(20px)';
    element.style.opacity = '0';
    setTimeout(() => {
      element.style.transition = `transform ${duration}ms ease, opacity ${duration}ms ease`;
      element.style.transform = 'translateY(0)';
      element.style.opacity = '1';
    }, 10);
  }

  static pulse(element) {
    element.style.animation = 'pulse 0.5s ease';
    setTimeout(() => element.style.animation = '', 500);
  }

  static bounce(element) {
    element.style.animation = 'bounce 0.5s ease';
    setTimeout(() => element.style.animation = '', 500);
  }

  static glow(element, color = '#6366F1') {
    element.style.boxShadow = `0 0 20px ${color}`;
    setTimeout(() => element.style.boxShadow = '', 1000);
  }

  static shake(element) {
    element.style.animation = 'shake 0.5s ease';
    setTimeout(() => element.style.animation = '', 500);
  }
}

// Premium Toast Notification
class PremiumToast {
  static show(message, type = 'success', duration = 3000) {
    const toast = document.createElement('div');
    toast.style.cssText = `
      position: fixed;
      top: 20px;
      right: 20px;
      left: 20px;
      max-width: 400px;
      margin: 0 auto;
      padding: 16px 20px;
      border-radius: 16px;
      background: ${type === 'success' ? 'linear-gradient(135deg, #10B981, #059669)' : type === 'error' ? 'linear-gradient(135deg, #EF4444, #DC2626)' : 'linear-gradient(135deg, #F59E0B, #D97706)'};
      color: white;
      font-weight: 700;
      font-size: 14px;
      box-shadow: 0 8px 30px rgba(0,0,0,0.3);
      z-index: 10000;
      display: flex;
      align-items: center;
      gap: 12px;
      animation: slideDown 0.3s ease;
    `;
    
    toast.innerHTML = `
      <span style="font-size: 20px;">${type === 'success' ? '✅' : type === 'error' ? '❌' : '⚠️'}</span>
      <span>${__piEscapeHtml(message)}</span>
    `;
    
    document.body.appendChild(toast);
    
    setTimeout(() => {
      toast.style.animation = 'slideUp 0.3s ease';
      setTimeout(() => toast.remove(), 300);
    }, duration);
  }
}

